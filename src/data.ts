export const buildWordList = (wordLists: Record<string, WordList>) => {
    const wordList: string[] = [];
    Object.values(wordLists).forEach((list) => {
        if (list.enable) {
            wordList.push(...list.data);
        }
    });

    return wordList;
};

export const getRandomWord = (array: string[]) => array[Math.floor(Math.random() * array.length)];
